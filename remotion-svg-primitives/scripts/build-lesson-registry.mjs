#!/usr/bin/env node
// Lesson-registry generator — code-as-truth for lesson composition registration.
//
// Statically scans src/lessons/Complete*Lesson.tsx for the OPT-IN marker
// `export const lessonComposition` and emits src/lessons/_lessonRegistry.generated.tsx, an array
// Root.tsx maps into <Composition> elements. A lesson that has not yet exported the descriptor is
// NOT imported (so half-built WIP never breaks the bundle), and the composer never hand-edits the
// shared Root.tsx / Composition.tsx registration lists — it just writes its own Complete<X>Lesson
// file with the descriptor, and the next `npm run lessons:registry` discovers it. This makes the
// worktree merge-back a conflict-free union of disjoint per-lesson files.
//
// Usage:
//   node scripts/build-lesson-registry.mjs           # (re)generate the manifest
//   node scripts/build-lesson-registry.mjs --check    # exit 1 if the manifest is stale (CI/pre-commit)
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(HERE, "..");
const LESSONS_DIR = path.join(REPO, "src/lessons");
const OUT = path.join(LESSONS_DIR, "_lessonRegistry.generated.tsx");
const check = process.argv.includes("--check");

// camelCase, unique import alias for each lesson's `lessonComposition` export.
const aliasFor = (base) => base.charAt(0).toLowerCase() + base.slice(1) + "Composition";

// Complete<Pascal>Lesson → the lesson's constPrefix (e.g. "kptestCountToTwo"),
// the convention every generated module + timeline is keyed by.
const constPrefixFor = (base) => {
  const core = base.replace(/^Complete/, "").replace(/Lesson$/, "");
  return core.charAt(0).toLowerCase() + core.slice(1);
};

const files = fs
  .readdirSync(LESSONS_DIR)
  .filter((f) => /^Complete.*Lesson\.tsx$/.test(f))
  .sort();

// Discover each opt-in lesson AND whether its reconciled timeline exposes a
// `<constPrefix>Cues` array — the source for the calculateMetadata duration
// (opportunity #5). A lesson missing that convention keeps its static
// durationInFrames (no wiring), so the generator never breaks on a non-conforming file.
const discovered = [];
for (const f of files) {
  const src = fs.readFileSync(path.join(LESSONS_DIR, f), "utf8");
  // OPT-IN: only lessons that export the uniform descriptor are registered (and thus imported).
  if (!/export\s+const\s+lessonComposition\b/.test(src)) continue;
  const base = path.basename(f, ".tsx");
  const constPrefix = constPrefixFor(base);
  const timelineBase = `${constPrefix}LessonTimeline`;
  const cuesExport = `${constPrefix}Cues`;
  const timelinePath = path.join(LESSONS_DIR, `${timelineBase}.ts`);
  let cues = null;
  if (fs.existsSync(timelinePath)) {
    const tsrc = fs.readFileSync(timelinePath, "utf8");
    if (new RegExp(`export\\s+const\\s+${cuesExport}\\b`).test(tsrc)) {
      cues = { cuesExport, timelineBase };
    }
  }
  discovered.push({ base, cues });
}

const wired = discovered.filter((d) => d.cues);

const body = [
  "// GENERATED by `npm run lessons:registry` (scripts/build-lesson-registry.mjs) — do NOT hand-edit.",
  "// Source of truth = each src/lessons/Complete*Lesson.tsx that exports `lessonComposition`.",
  "// Add a lesson by exporting that descriptor from its own file; never edit this file or Root.tsx.",
  "// durationInFrames is derived via Remotion calculateMetadata from each lesson's generated <X>Cues array (opportunity #5).",
  'import type { LessonComposition } from "./lessonRegistryTypes";',
  ...(wired.length ? ['import { durationFromCues } from "./_cues/durationFromCues";'] : []),
  ...discovered.map((d) => `import { lessonComposition as ${aliasFor(d.base)} } from "./${d.base}";`),
  ...wired.map((d) => `import { ${d.cues.cuesExport} } from "./${d.cues.timelineBase}";`),
  "",
  "export const AUTO_LESSON_COMPOSITIONS: LessonComposition[] = [",
  ...discovered.map((d) =>
    d.cues
      ? `  { ...${aliasFor(d.base)}, calculateMetadata: () => ({ durationInFrames: durationFromCues(${d.cues.cuesExport}) }) },`
      : `  ${aliasFor(d.base)},`,
  ),
  "];",
  "",
].join("\n");

if (check) {
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, "utf8") : "";
  if (current !== body) {
    console.error(
      `lessons:registry --check FAILED — ${path.relative(REPO, OUT)} is stale.\n` +
        `Run \`npm run lessons:registry\`. Discovered ${discovered.length} opt-in lesson(s).`,
    );
    process.exit(1);
  }
  console.log(`lessons:registry --check ok — ${discovered.length} auto-registered lesson(s) in sync.`);
} else {
  fs.writeFileSync(OUT, body);
  const names = discovered.map((d) => d.base).join(", ");
  console.log(
    `lessons:registry ok — wrote ${path.relative(REPO, OUT)} with ${discovered.length} lesson(s) ` +
      `(${wired.length} calculateMetadata-wired): ${names || "(none yet)"}`,
  );
}
